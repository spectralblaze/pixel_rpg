# ── Synthesis constants ───────────────────────────────────────────────────────
SR     = 22050
A_MEL  = 13000
A_BASS =  8000
A_CTR  =  6000
GAP    = 0.08   # silence gap between loops (seconds)

# ── Chromatic pitch table ─────────────────────────────────────────────────────
_P = {
    'C': 0, 'C#': 1, 'Db': 1, 'D': 2, 'D#': 3, 'Eb': 3, 'E': 4,
    'F': 5, 'F#': 6, 'Gb': 6, 'G': 7, 'G#': 8, 'Ab': 8,
    'A': 9, 'A#': 10, 'Bb': 10, 'B': 11,
}


def _midi(name: str) -> int:
    if name == 'R':
        return 0
    octave = int(name[-1])
    return 12 + octave * 12 + _P[name[:-1]]


def _freq(midi: int) -> float:
    return 440.0 * (2.0 ** ((midi - 69) / 12.0))


def _one_period(freq: float, wave: str, amp: int) -> list:
    n = max(2, int(SR / freq))
    out = []
    for i in range(n):
        t = i / n
        if   wave == 'pulse':    v =  1.0 if t < 0.25 else -1.0
        elif wave == 'triangle': v =  1.0 - abs(t - 0.5) * 4
        elif wave == 'sine':     v =  math.sin(2 * math.pi * t)
        elif wave == 'warm':
            v = math.sin(2 * math.pi * t) * 0.6 + (1.0 - abs(t - 0.5) * 4) * 0.4
        else:                    v =  1.0 if t < 0.5 else -1.0
        out.append(int(v * amp))
    return out


def _note(midi: int, dur_s: float, wave: str, amp: int,
          articulation: float = 0.82) -> list:
    n = int(dur_s * SR)
    if n <= 0:
        return []
    if midi == 0:
        return [0] * n
    tone_n = max(4, int(n * articulation))
    gap_n  = n - tone_n
    cyc  = _one_period(_freq(midi), wave, amp)
    clen = len(cyc)
    raw  = list((cyc * (tone_n // clen + 2))[:tone_n])
    atk  = max(2, tone_n *  3 // 100)
    dec  = max(2, tone_n *  6 // 100)
    rel  = max(2, tone_n * 13 // 100)
    sus  = 0.78
    for i in range(atk):
        raw[i] = int(raw[i] * i / atk)
    for i in range(dec):
        raw[atk + i] = int(raw[atk + i] * (1.0 - (i / dec) * (1.0 - sus)))
    sus_start = atk + dec
    sus_end   = max(sus_start, tone_n - rel)
    for i in range(sus_start, sus_end):
        raw[i] = int(raw[i] * sus)
    for i in range(rel):
        idx = sus_end + i
        if idx < tone_n:
            raw[idx] = int(raw[idx] * sus * (rel - i) / rel)
    return raw + [0] * gap_n


def _render(notes: list, bpm: float, wave: str, amp: int,
            pad_to: int = None, articulation: float = 0.82) -> list:
    beat_s = 60.0 / bpm
    out = []
    for name, beats in notes:
        out.extend(_note(_midi(name), beats * beat_s, wave, amp, articulation))
    out.extend([0] * int(GAP * SR))
    if pad_to is not None:
        if len(out) < pad_to:
            out += [0] * (pad_to - len(out))
        else:
            out = out[:pad_to]
    return out


def _mix_lists(*layers) -> list:
    """Additively mix N int-lists; clamp to ±32000; return a list."""
    n = max(len(L) for L in layers)
    result = [0] * n
    for L in layers:
        for i in range(len(L)):
            result[i] += L[i]
    for i in range(n):
        v = result[i]
        if   v >  32000: result[i] =  32000
        elif v < -32000: result[i] = -32000
    return result


def _to_sound(*layers) -> 'pygame.mixer.Sound':
    mono = _mix_lists(*layers)
    n    = len(mono)
    info = pygame.mixer.get_init()
    channels = info[2] if info else 1
    if channels == 2:
        stereo       = [0] * (2 * n)
        stereo[::2]  = mono
        stereo[1::2] = mono
        buf = array.array('h', stereo)
    else:
        buf = array.array('h', mono)
    return pygame.mixer.Sound(buffer=bytes(buf))


