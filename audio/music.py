# ── Music manager ─────────────────────────────────────────────────────────────

class MusicManager:
    FADE_MS = 800

    def __init__(self):
        self._ok      = False
        self._ch_mel  = None
        self._ch_bass = None
        self._cache: dict = {}
        self._current: str | None = None
        self._volume  = 0.65

        try:
            if pygame.mixer.get_init() is None:
                raise RuntimeError("mixer not initialised")
            pygame.mixer.set_reserved(2)
            self._ch_mel  = pygame.mixer.Channel(0)
            self._ch_bass = pygame.mixer.Channel(1)
            self._ok = True
        except Exception as exc:
            print(f"[Music] Audio disabled: {exc}")

    # ── Building ──────────────────────────────────────────────────────────────

    def _build(self, key: str) -> tuple:
        td     = TRACKS[key]
        bpm    = td['bpm']
        mel_w  = td.get('mel_w',  'triangle')
        bass_w = td.get('bass_w', 'triangle')
        ctr_w  = td.get('ctr_w',  'sine')

        if 'sections' in td:
            all_mel  = []
            all_bass = []
            for sec in td['sections']:
                sw   = sec.get('mel_w',  mel_w)
                bw   = sec.get('bass_w', bass_w)
                cw   = sec.get('ctr_w',  ctr_w)
                beats   = sum(b for _, b in sec['mel'])
                total_n = int(beats * 60.0 / bpm * SR)
                mel_raw  = _render(sec['mel'],  bpm, sw, A_MEL,  total_n, 0.82)
                bass_raw = _render(sec['bass'], bpm, bw, A_BASS, total_n, 0.94)
                if 'ctr' in sec:
                    ctr_raw = _render(sec['ctr'], bpm, cw, A_CTR, total_n, 0.88)
                    all_mel.extend(_mix_lists(mel_raw, ctr_raw))
                else:
                    all_mel.extend(mel_raw)
                all_bass.extend(bass_raw)
            gap = [0] * int(GAP * SR)
            all_mel.extend(gap)
            all_bass.extend(gap)
            mel_snd  = _to_sound(all_mel)
            bass_snd = _to_sound(all_bass)
        else:
            beats   = sum(b for _, b in td['mel'])
            total_n = int((beats * 60.0 / bpm + GAP) * SR)
            mel_raw  = _render(td['mel'],  bpm, mel_w,  A_MEL,  total_n, 0.82)
            bass_raw = _render(td['bass'], bpm, bass_w, A_BASS, total_n, 0.94)
            if 'ctr' in td:
                ctr_raw = _render(td['ctr'], bpm, ctr_w, A_CTR, total_n, 0.88)
                mel_snd = _to_sound(mel_raw, ctr_raw)
            else:
                mel_snd = _to_sound(mel_raw)
            bass_snd = _to_sound(bass_raw)

        mel_snd.set_volume(self._volume)
        bass_snd.set_volume(self._volume * 0.70)
        return mel_snd, bass_snd

    def _get(self, key: str) -> tuple:
        if key not in self._cache:
            wav_path = os.path.join(TRACKS_DIR, f"{key}.wav")
            if os.path.isfile(wav_path):
                try:
                    snd = pygame.mixer.Sound(wav_path)
                    snd.set_volume(self._volume)
                    self._cache[key] = (snd, None)
                    print(f"[Music] Override: {wav_path}")
                except Exception as exc:
                    print(f"[Music] Override load failed '{wav_path}': {exc}")
            if key not in self._cache:
                try:
                    self._cache[key] = self._build(key)
                except Exception as exc:
                    print(f"[Music] Build failed '{key}': {exc}")
                    return None, None
        return self._cache[key]

    # ── Playback ──────────────────────────────────────────────────────────────

    def play(self, key: str, loops: int = -1):
        if not self._ok or key not in TRACKS:
            return
        if self._current == key and loops == -1:
            return
        mel_s, bass_s = self._get(key)
        if mel_s is None:
            return
        self._current = key
        self._ch_mel.play(mel_s, loops=loops, fade_ms=self.FADE_MS)
        if bass_s is not None:
            self._ch_bass.play(bass_s, loops=loops, fade_ms=self.FADE_MS)
        else:
            self._ch_bass.fadeout(self.FADE_MS)

    def stop(self):
        if not self._ok:
            return
        self._current = None
        self._ch_mel.fadeout(self.FADE_MS)
        self._ch_bass.fadeout(self.FADE_MS)

    def set_volume(self, v: float):
        self._volume = max(0.0, min(1.0, v))
        for mel_s, bass_s in self._cache.values():
            mel_s.set_volume(self._volume)
            if bass_s is not None:
                bass_s.set_volume(self._volume * 0.70)

    # ── Convenience helpers ───────────────────────────────────────────────────

    def play_menu(self):
        self.play('menu')

    def play_explore(self, biome_id: str):
        key = biome_id if biome_id in TRACKS else 'verdant_plains'
        self.play(key)

    def play_battle(self, biome_id: str):
        key = BATTLE_THEME.get(biome_id, 'battle_verdant_plains')
        self.play(key)

    def play_alpha(self, biome_id: str = ''):
        """Hyper-intense music for alpha variant encounters."""
        self.play('alpha')

    def play_boss(self, boss_id: str = None):
        """Play the unique theme for *boss_id*, fallback to golem_king."""
        key = BOSS_THEME.get(boss_id, 'boss_golem_king')
        self.play(key)

    def play_victory(self):
        self._current = 'victory'
        if not self._ok:
            return
        mel_s, bass_s = self._get('victory')
        if mel_s is None:
            return
        self._ch_mel.play(mel_s, loops=0, fade_ms=120)
        if bass_s is not None:
            self._ch_bass.play(bass_s, loops=0, fade_ms=120)
        else:
            self._ch_bass.fadeout(120)

    def play_gameover(self):
        self._current = 'gameover'
        if not self._ok:
            return
        mel_s, bass_s = self._get('gameover')
        if mel_s is None:
            return
        self._ch_mel.play(mel_s, loops=0, fade_ms=300)
        if bass_s is not None:
            self._ch_bass.play(bass_s, loops=0, fade_ms=300)
        else:
            self._ch_bass.fadeout(300)

    def play_item_found(self):
        """Short jingle when a rare item or skill is discovered (~15 s)."""
        if not self._ok:
            return
        saved = self._current
        mel_s, bass_s = self._get('item_found')
        if mel_s is None:
            return
        # Play over current music on a one-shot basis, then resume
        self._ch_mel.play(mel_s, loops=0, fade_ms=80)
        if bass_s is not None:
            self._ch_bass.play(bass_s, loops=0, fade_ms=80)
        else:
            self._ch_bass.fadeout(80)
        # Note: caller can resume exploration music when jingle finishes
        self._current = saved
